public class Main {
    public static void main(String[] args) {
        int numberOne = 30;
        int numberTwo = 30;

        if (numberOne == numberTwo) {
            System.out.println(numberOne + " = " + numberTwo);
        } else if (numberOne > numberTwo) {
            System.out.println(numberOne + " > " + numberTwo);
        } else {
            System.out.println(numberOne + " < " + numberTwo);
        }
    }
}